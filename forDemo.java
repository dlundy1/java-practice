/*
 * Demonstrates the 'for' Loop.
 * Call this file forDemo.java 
*/

class forDemo {
  public static void main(String args[]) {
	int count;
	
	for(count = 5; count > 0; count--)
	  System.out.println("Count is: " + count);
	  
	System.out.println("Done!"); // executes once Loop is Complete.
  }
}
